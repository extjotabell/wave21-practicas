public class Circuito {
    public int id_circuito;
    public String nombre_circuito;
    public String descripcion_circuito;

    public Circuito(int id_circuito, String nombre_circuito, String descripcion_circuito) {
        this.id_circuito = id_circuito;
        this.nombre_circuito = nombre_circuito;
        this.descripcion_circuito = descripcion_circuito;
    }
}
